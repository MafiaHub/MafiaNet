#pragma once
#include "mafianet/crypto/keys.h"
#include <cstring>

namespace MafiaNet {
// Fixed, well-known DEMO server keypair for samples ONLY. NOT secret; do not use in production.
// secretKey = 0x00..0x1f ; publicKey = X25519 base point * secretKey.
inline ServerSecurityKey GetSampleServerKey()
{
	ServerSecurityKey k;
	static const unsigned char sec[32] = {
		0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f,
		0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17,0x18,0x19,0x1a,0x1b,0x1c,0x1d,0x1e,0x1f };
	static const unsigned char pub[32] = {
		0x8f,0x40,0xc5,0xad,0xb6,0x8f,0x25,0x62,0x4a,0xe5,0xb2,0x14,0xea,0x76,0x7a,0x6e,
		0xc9,0x4d,0x82,0x9d,0x3d,0x7b,0x5e,0x1a,0xd1,0xba,0x6f,0x3e,0x21,0x38,0x28,0x5f };
	memcpy(k.secretKey, sec, 32);
	memcpy(k.publicKey, pub, 32);
	return k;
}
} // namespace MafiaNet
